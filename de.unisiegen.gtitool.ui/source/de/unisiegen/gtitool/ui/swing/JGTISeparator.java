package de.unisiegen.gtitool.ui.swing;


import javax.swing.JSeparator;


/**
 * Special {@link JSeparator}.
 * 
 * @author Christian Fehler
 * @version $Id$
 */
public final class JGTISeparator extends JSeparator
{

  /**
   * The serial version uid.
   */
  private static final long serialVersionUID = 4977904198184229890L;


  /**
   * Allocates a new {@link JGTISeparator}.
   */
  public JGTISeparator ()
  {
    super ();
  }


  /**
   * Allocates a new {@link JGTISeparator}.
   * 
   * @param orientation The orientation.
   */
  public JGTISeparator ( int orientation )
  {
    super ( orientation );
  }
}
